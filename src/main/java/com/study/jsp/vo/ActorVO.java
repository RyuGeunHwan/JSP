package com.study.jsp.vo;

public class ActorVO {
	
	public String name;
	public int age;
	private String addr;
	private String job;
	
	
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	
}
